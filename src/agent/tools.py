"""src.agent.tools.py

Модуль предоставляет набор инструментов (tools) для агента DocAgent‑mini,
интегрируемых с графом LangGraph.

Инструменты предназначены для использования в workflow графа LangGraph
и декорированы @tool для корректной интеграции с LangChain.
"""


from langchain_core.tools import tool

from src.logger import logger
from src.rag.rag_system import RAGSystem


def create_search_tool(rag_system: RAGSystem):
    """
    Создаёт инструмент поиска для интеграции с LangGraph.

    Возвращает асинхронную функцию search, настроенную на работу
    с переданным экземпляром RAGSystem для выполнения поисковых запросов.

    Args:
        rag_system (RAGSystem): экземпляр системы RAG, используемый
            для выполнения поисковых запросов к векторной базе данных.
            Должен быть предварительно инициализирован с нужными настройками.

    Returns:
        Callable[[str], Awaitable[str]]: асинхронная функция search
            для использования в графе workflow. Принимает строку‑запрос
            и возвращает строку с найденным контекстом.

    Example:
        rag = RAGSystem(settings)
        search_tool = create_search_tool(rag)
        # search_tool теперь можно передать в граф LangGraph

    Note:
        Функция создаёт инструмент, привязанный к конкретному экземпляру
        RAGSystem. Убедитесь, что rag_system корректно настроен
        перед вызовом create_search_tool.
    """
    @tool
    async def search(request: str) -> str:
        """
        Инструмент поиска контекста в векторной базе данных.

        Вызывает метод search у связанного экземпляра RAGSystem
        для поиска релевантной информации по запросу пользователя.

        Args:
            request (str): текстовый запрос пользователя для поиска.
                Может содержать вопрос, ключевую фразу или набор ключевых слов.

        Returns:
            str: найденный контекст из векторной базы данных.
                Содержит релевантную информацию, извлечённую на основе запроса.
                Если совпадений не найдено, может вернуть пустую строку
                или сообщение об отсутствии результатов
                (зависит от реализации RAGSystem).

        Raises:
            Exception: при ошибках взаимодействия с RAGSystem или векторной БД,
                например:
                - проблемы подключения к БД;
                - ошибки выполнения поискового запроса;
                - таймауты при ожидании ответа;
                - некорректный формат запроса.

        Note:
            Функция использует внешний экземпляр RAGSystem, переданный
            в create_search_tool. Убедитесь, что rag_system корректно
            настроен и инициализирован перед созданием инструмента.
        """
        logger.debug('Запуск search')
        context = await rag_system.search(request)
        logger.debug(f'Получен контекст: {context}')
        return context
    return search


@tool
async def create_task_id(task_id: int | None = None) -> int:
    """
    Создаёт задачу и возвращает её ID.

    Генерирует уникальный идентификатор задачи по следующим правилам:
    - если task_id не указан (None), начинает нумерацию с 0;
    - если передан task_id, возвращает task_id + 1.

    Args:
        task_id (int | None): текущий идентификатор задачи.
            Если None, нумерация начинается с 0. По умолчанию — None.

    Returns:
        int: новый идентификатор задачи (целое число).

    Example:
        create_task_id() -> 1
        create_task_id(5) -> 6
        create_task_id(None) -> 1

    Note:
        Функция всегда увеличивает переданный ID на 1, даже если он равен 0.
        Это обеспечивает последовательную нумерацию задач без пропусков.
    """
    logger.debug('Запуск create_task_id')
    if task_id is None:
        task_id = 0
    task_id += 1
    logger.debug(f'Создан task_id: {task_id}')
    return task_id


@tool
async def add_comment(task_id: int | None, comment: str) -> str:
    """
    Добавляет комментарий к задаче с указанным идентификатором.

    Проверяет валидность task_id (не None и не 0). Если идентификатор
    некорректен, возвращает сообщение об отсутствии текущей задачи.
    В противном случае формирует и возвращает подтверждение успешного
    добавления.

    Args:
        task_id (int | None): идентификатор задачи, к которой добавляется
            комментарий. Должен быть ненулевым положительным числом.
            Если передан None или 0, функция вернёт сообщение об ошибке.
        comment (str): текст комментария, который нужно добавить к задаче.

    Returns:
        str: строка-статус результата операции:
            - 'комментарий добавлен' — если комментарий успешно добавлен к
            задаче с корректным ID;
            - 'комментарий не добавлен, нет созданных задач' — если task_id
            равен None или 0.
    """
    logger.debug('Запуск add_comment')
    if task_id is None or task_id == 0:
        logger.debug('комментарий не добавлен')
        return 'комментарий не добавлен, нет созданных задач'
    result = f'Комментарий "{comment}" добавлен к задаче {task_id}'
    logger.debug(f'Создан комментарий: {result}')
    return 'комментарий добавлен'


def TOOLS(rag_system: RAGSystem) -> list:
    """
    Формирует список инструментов для агента на основе переданного экземпляра
    RAGSystem.

    Включает три основных инструмента: поиск, создание задач и добавление
    комментариев.

    Args:
        rag_system (RAGSystem): экземпляр RAG-системы, используемый
            для создания инструмента поиска (search). Должен быть
            предварительно инициализирован.

    Returns:
        list: список доступных инструментов агента. Содержит:
            1. search: инструмент поиска через RAGSystem (создаётся
               динамически с привязкой к переданному rag_system);
            2. create_task_id: инструмент создания задач с автоинкрементным ID;
            3. add_comment: инструмент добавления комментариев к существующим
                задачам.
    """
    return [create_search_tool(rag_system), create_task_id, add_comment]
